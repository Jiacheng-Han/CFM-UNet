class VSSM(nn.Module):
    def __init__(self, patch_size=4, in_chans=3, num_classes=1, depths=[3, 3, 3, 3],
                 dims=[64, 128, 256, 512], d_state=16, drop_rate=0., attn_drop_rate=0., drop_path_rate=0.1,
                 norm_layer=nn.LayerNorm, patch_norm=True,
                 use_checkpoint=False):
        super().__init__()
        self.num_layers = len(depths)
        if isinstance(dims, int):
            dims = [int(dims * 2 ** i_layer) for i_layer in range(self.num_layers)]
        self.embed_dim = dims[0]
        self.dims = dims
        self.num_classes=num_classes

        self.patch_embed = PatchEmbed2D(patch_size=patch_size, in_chans=in_chans, embed_dim=self.embed_dim,
                                        norm_layer=norm_layer if patch_norm else None)

        self.pos_drop = nn.Dropout(p=drop_rate)

        dpr = [x.item() for x in torch.linspace(0, drop_path_rate, sum(depths))]

        self.layers = nn.ModuleDict()  # 使用 ModuleDict 来保存层，并命名为 X1, X2, X3, X4
        for i_layer in range(self.num_layers):
            layer_name = f'X{i_layer + 1}'  # 构建层的名称，例如 X1, X2, X3, X4
            layer = VSSLayer(
                dim=dims[i_layer],
                depth=depths[i_layer],
                d_state=d_state,
                drop=drop_rate,
                attn_drop=attn_drop_rate,
                drop_path=dpr[sum(depths[:i_layer]):sum(depths[:i_layer + 1])],
                norm_layer=norm_layer,
                downsample=PatchMerging2D,
                use_checkpoint=use_checkpoint,
            )
            self.layers[layer_name] = layer  # 将层加入 ModuleDict 中，以名称作为键

        self.apply(self._init_weights)

    def _init_weights(self, m: nn.Module):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)

    def forward(self, x):
        if x.size()[1] == 1:
            x = x.repeat(1,3,1,1)
        x = self.patch_embed(x)
        x = self.pos_drop(x)
        print(x.size())
        # 依次调用四个键对应的网络
        x = self.layers['X1'](x)
        x = self.layers['X2'](x)
        x = self.layers['X3'](x)
        x = self.layers['X4'](x)

        if self.num_classes == 1: 
            return torch.sigmoid(x)
        else: 
            return x

        
class SELayer(nn.Module):
    def __init__(self, channel, reduction=16):
        super(SELayer, self).__init__()
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.fc = nn.Sequential(
            nn.Linear(channel, channel // reduction, bias=False),
            nn.ReLU(inplace=True),
            nn.Linear(channel // reduction, channel, bias=False),
            nn.Sigmoid()
        )

    def forward(self, x):
        b, c, _, _ = x.size()
        y = self.avg_pool(x).view(b, c)
        y = self.fc(y).view(b, c, 1, 1)
        return x * y.expand_as(x)

class Fusion(nn.Module):
    def __init__(self, input_channels, output_channels):
        super(Fusion, self).__init__()
        # Define your fusion layers here
        self.conv1 = nn.Conv2d(input_channels * 2, output_channels, kernel_size=3, padding=1)
        self.bn1 = nn.BatchNorm2d(output_channels)
        self.se = SELayer(output_channels)

    def forward(self, x1, x2):
        # x1: input from the first pathway
        # x2: input from the second pathway
        # Perform fusion
        fusion_input = torch.cat((x1, x2), dim=1)
        fusion_output = self.conv1(fusion_input)
        fusion_output = self.bn1(fusion_output)
        fusion_output = torch.sigmoid(fusion_output)
        
        # Apply SE attention mechanism
        fusion_output = self.se(fusion_output)
        
        return fusion_output

    
class DoubleConv(nn.Module):
    def __init__(self, in_channels, out_channels, dropout_rate=0.5):
        super(DoubleConv, self).__init__()
        self.conv1 = nn.Conv2d(in_channels, out_channels, kernel_size=3, padding=1)
        self.bn1 = nn.BatchNorm2d(out_channels)
        self.relu1 = nn.ReLU(inplace=True)

        self.conv2 = nn.Conv2d(out_channels, out_channels, kernel_size=3, padding=1)
        self.bn2 = nn.BatchNorm2d(out_channels)
        self.relu2 = nn.ReLU(inplace=True)

        self.dropout = nn.Dropout2d(dropout_rate)

    def forward(self, x):
        x = self.conv1(x)
        x = self.bn1(x)
        x = self.relu1(x)

        x = self.conv2(x)
        x = self.bn2(x)
        x = self.relu2(x)

        x = self.dropout(x)

        return x
    
